# Кейс-задача № 1
# Дан одномерный массив A размерности N.
# Найти сумму отрицательных элементов, расположенных
# между максимальным и минимальным элементами массива.

import random


def sum_negative_between_max_min(a: list) -> int:
    """Возвращает сумму отрицательных элементов массива,
    расположенных строго между максимальным и минимальным элементами."""
    if len(a) < 3:
        return 0

    i_max = a.index(max(a))   # индекс первого максимального элемента
    i_min = a.index(min(a))   # индекс первого минимального элемента

    # определяем границы участка между max и min
    left, right = min(i_max, i_min), max(i_max, i_min)

    # суммируем только отрицательные элементы строго между границами
    return sum(x for x in a[left + 1:right] if x < 0)


if __name__ == "__main__":
    n = int(input("Введите размерность массива N: "))
    a = [random.randint(-50, 50) for _ in range(n)]

    print("Исходный массив A:", a)
    print("Максимальный элемент:", max(a), "с индексом", a.index(max(a)))
    print("Минимальный элемент:", min(a), "с индексом", a.index(min(a)))
    print("Сумма отрицательных элементов между max и min:",
          sum_negative_between_max_min(a))
